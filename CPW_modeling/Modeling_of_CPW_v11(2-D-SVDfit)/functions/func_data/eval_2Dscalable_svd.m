function [ op] = eval_2Dscalable_svd( x,y,coeffs,order)
%UNTITLED3 Summary of this function goes here
%   Detailed explanation goes here
order_str = [num2str(order(1)),num2str(order(2))];
sf33 = [1,...
        x,y,...
        x^2,x*y,y^2,...
        x^3,(x^2)*y,x*y^2,y^3];
sf44 = [1,...
        x,y,...
        x^2,x*y,y^2,...
        x^3,(x^2)*y,x*y^2,y^3,...
        x^4,(x^3)*y,(x^2)*(y^2),x*y^3,y^4];
sf55 = [1,...
        x,y,...
        x^2,x*y,y^2,...
        x^3,(x^2)*y,x*y^2,y^3,...
        x^4,(x^3)*y,(x^2)*(y^2),x*y^3,y^4,...
        x^5,(x^4)*y,(x^3)*(y^2),(x^2)*y^3,x*(y^4),y^5];

switch order_str
    case '33'
        sf = sf33 ;
    case '44'
        sf = sf44;
    case '55'
        sf = sf55;
    otherwise
        error('Error: the order should be one of [''33'' ''44'' ''55'' ')     
end
op = sf*coeffs;
end

